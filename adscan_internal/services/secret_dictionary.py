def is_dictionary_word(value: str) -> bool:
    """Return True if value is a known natural-language dictionary word.

    Conservative: only flags exact lowercase matches. Compound words like
    ``socioadicciones``, brand+city like ``aisbarcelona``, or mixed-case
    strings like ``Ais_barcelona1`` return False (no penalty) because they
    are more likely real passwords.

    Args:
        value: Candidate string to test.

    Returns:
        True when the lowercase form of ``value`` is in the embedded corpus.
    """
    if not value:
        return False
    return value.lower() in _COMMON_WORDS


